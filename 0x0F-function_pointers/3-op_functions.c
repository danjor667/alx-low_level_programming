#include <stdlib.h>
/**
 * op_add- a function to add 2 int
 * @a:inut int
 * @b:input int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub- func to find the diff of 2 int
 * @a: input int
 * @b: input int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- function to multiply 2 numbers
 * @a: input
 * @b: input int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div- funct to divide 2 numbers
 * @a: input
 * @b: input
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- funct to find the modulus of 2 num
 * @a: input
 * @b: input
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}

